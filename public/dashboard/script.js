const socket = io();

let programList = [];

socket.on("programs", (programs) => {
  programList = programs;
  updateProcessList();
});

function getProcessInfo() {
  return programList;
}

function updateProcessList() {
  const processList = document.getElementById("process-list-body");
  processList.innerHTML = "";

  const processes = getProcessInfo();

  processes.forEach((process) => {
    const row = document.createElement("tr");
    row.innerHTML = `
          <td>${process.name}</td>
          <td>${process.port}</td>
          <td><button class="inspect-btn">Inspect</button></td>
      `;
    processList.appendChild(row);

    const inspectButton = row.querySelector(".inspect-btn");
    inspectButton.addEventListener("click", () => {
      document.getElementById("transitionDiv").style.opacity = 1;
      setTimeout(
        () => (window.location.href = "/inspect/" + process.uuid),
        500
      );
    });
  });
}
const addProcessForm = document.querySelector(".add-process form");

addProcessForm.addEventListener("submit", (e) => {
  e.preventDefault();
  const processName = document.getElementById("process-name").value;
  const processGitHub = document.getElementById("process-github").value;
  const processZip = document.getElementById("process-zip").files[0];
  const processPort = document.getElementById("process-port").value;
  if (processGitHub) {
    socket.emit("github", {
      url: processGitHub,
      name: processName,
      port: processPort,
    });
  } else if (processZip) {
    handleZipUpload(processZip, processName, processPort);
  }
  addProcessForm.reset();
  document.getElementById("add-program").disabled = true;
  document.getElementById("add-program").innerHTML = "...";
});

function handleZipUpload(zipFile, programName, programPort) {
  const reader = new FileReader();

  reader.onload = function (event) {
    socket.emit("upload", {
      file: event.target.result,
      name: programName,
      port: programPort,
    });
  };

  reader.readAsArrayBuffer(zipFile);
}

socket.on("uploadResult", (result) => {
  if (result) {
    Swal.fire({
      title: "Success!",
      icon: "success",
    }).then(() => {
      window.location.href = "/";
    });
  } else {
    Swal.fire({
      title: "Something went wrong!",
      icon: "error",
    }).then(() => {
      window.location.href = "/";
    });
  }
});

